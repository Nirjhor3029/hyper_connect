{{-- Photo Guidelines --}}
<div id="photo-guidelines" class="instruction-content" style="display: none;">
    <div class="row">
        <div class="col-main col-xs-12 col-sm-12">
            <div class="padding-s">
                <div class="page-title category-title">
                    <h1>Passport Photo Guidelines</h1>
                </div>




                <div class="body-container">
                    <p>Please find below the photo guidelines for submitting a passport photo to EMGS. Kindly note that
                        this
                        is important since the same picture will be used on the Immigration System as well as printed on
                        the
                        iKad.</p>
                    <br>
                    <p>Institutions will be requested to adhere to the guidelines below to avoid any delay in processing
                        as
                        well as avoiding any untoward issues with law enforcement officials checking the iKad.</p>
                    <br>
                    <p>Your photo must be taken by a professional photographer in a photo studio to ensure high quality,
                        proper lighting, and compliance with all the specified requirements.</p>
                    <ul style="list-style-type: decimal;">
                        <li>
                            <b>In Color</b>: Your photo must be in color and not black and white.
                        </li>
                        <li>
                            <b>White Background</b>: The background for the photo must be plain white to ensure that you
                            stand out clearly.
                        </li>
                        <li>
                            <b>Size Specifications</b>: The photo must be 45mm high and 35mm wide, should not be trimmed
                            or
                            altered from a larger image to fit these dimensions.
                        </li>
                        <br>
                        <table width="100%">
                            <tbody>
                                <tr>
                                    <td width="50%" style="text-align:center;">
                                        <img class="passport-mobile"
                                            src="https://visa.educationmalaysia.gov.my/skin/frontend/emgs/default/images/photo3.png"
                                            alt="pict">
                                    </td>
                                    <td width="50%" style="text-align:center;">
                                        <img class="passport-mobile"
                                            src="https://visa.educationmalaysia.gov.my/skin/frontend/emgs/default/images/photo4.png"
                                            alt="pict">
                                    </td>
                                </tr>
                            </tbody>
                        </table>
                        <br>
                        <li>
                            <b>No Editing, No Selfies</b>: The photo should not be digitally altered in any way (e.g.,
                            no
                            filters or image enhancements) and should not be self-taken.
                        </li>
                        <li>
                            <b>No Eyeglasses</b>: You should remove your glasses to avoid glare or reflection and to
                            make
                            sure your eyes are clearly visible.
                        </li>
                        <li>
                            <b>Clear Contrast</b>: You should stand out from the background, meaning your clothes should
                            not
                            be of a similar color to the background.
                        </li>
                        <li>
                            <b>Free from Shadows</b>: The lighting should be even and balanced to prevent shadows on the
                            face or background.
                        </li>
                        <li>
                            <b>Facing Forward</b>: You must be facing the camera directly, with face and shoulders
                            squarely
                            aligned towards the camera.
                        </li>
                        <li>
                            <b>Lips Closed</b>: Your mouth should be closed so that the facial features are neutrally
                            presented.
                        </li>
                        <li>
                            <b>Individual Photo</b>: The photo must contain only the person applying for visa; no other
                            people or objects should be in the picture.
                        </li>
                        <li>
                            <b>Headgear Rules</b>: No hats or head coverings, except for those worn for religious
                            reasons,
                            and they should not obscure any facial features.
                        </li>
                        <li>
                            <b>Free from 'Redeye'</b>: The photo should not have 'redeye', which is often caused by
                            flash
                            photography.
                        </li>
                        <li>
                            <b>Facial Features Visible</b>: The entire face should be visible from the bottom of the
                            chin to
                            the top of the head (crown), including the hairline.
                        </li>
                        <li>
                            <b>Recent Photo</b>: The photograph must be taken within the last six months to ensure it
                            reflects your current appearance.
                        </li>
                    </ul>
