{{-- Photo Guidelines --}}
<div id="photo-guidelines" class="instruction-content" style="display: none;">
    <h5>Passport Photo Guidelines:</h5>
    <h1 class="content-title">Passport Photo Guidelines</h1>

    <p class="content-text">
        Please find below the photo guidelines for submitting a passport photo to EMGS.
        Kindly note that this is important since the same picture will be used on the
        Immigration System as well as printed on the iKad.
    </p>

    <p class="content-text">
        Institutions will be requested to adhere to the guidelines below to avoid any delay
        in processing as well as avoiding any untoward issues with law enforcement officials
        checking the iKad.
    </p>

    <p class="content-text">
        Your photo must be taken by a professional photographer in a photo studio to ensure
        high quality, proper lighting, and compliance with all the specified requirements.
    </p>

    <ol class="guideline-list">
        <li><strong>In Color:</strong> Your photo must be in color and not black and white.
        </li>
        <li><strong>White Background:</strong> The background for the photo must be plain
            white to ensure that you stand out clearly.</li>
        <li><strong>Size Specifications:</strong> The photo must be 45mm high and 35mm wide,
            should not be trimmed or altered from a larger image to fit these dimensions.
        </li>
    </ol>

    <div class="photo-examples">
        <div class="photo-example">
            <div class="photo-box">
                <div class="photo-silhouette">
                    <div class="check-mark">✓</div>
                </div>
            </div>
            <div class="dimension-label">
                <strong>45mm × 35mm</strong>
            </div>
        </div>
        <div class="photo-example">
            <div class="photo-box">
                <div class="photo-silhouette">
                    <div class="x-mark">✗</div>
                </div>
            </div>
            <div class="dimension-label">
                <strong>45mm × 35mm</strong>
            </div>
        </div>
    </div>

    <ol class="guideline-list" start="4">
        <li><strong>No Editing, No Selfies:</strong> The photo should not be digitally
            altered in any way (e.g., no filters or image enhancements) and should not be
            self-taken.</li>
        <li><strong>No Eyeglasses:</strong> You should remove your glasses to avoid glare or
            reflection and to make sure your eyes are clearly visible.</li>
        <li><strong>Clear Contrast:</strong> You should stand out from the background,
            meaning your clothes should not be of a similar color to the background.</li>
        <li><strong>Free from Shadows:</strong> The lighting should be even and balanced to
            prevent shadows on the face or background.</li>
        <li><strong>Facing Forward:</strong> You must be facing the camera directly, with
            face and shoulders squarely aligned towards the camera.</li>
    </ol>
</div>

